            <h2 class="describe">Quality Engineered Wood Flooring by Just Wood Flooring</h2>
            <p>In certain cases, engineered floor boards can also offer value advantages in terms of both product cost and fitting times.</p>
            <p>Engineered single strip planks (once laid) give no clue as to the plywood substrate beneath their beautiful solid wood wear layer construction. They look in every way exactly the same as traditional solid wood floor boards.</p> 
            <p>Engineered wooden floor boards are less prone to sectional movement then their solid hardwood counterparts and therefor do not swell or contract to the same extent as solid wood flooring, when exposed to changes in relative humidity.</p> 
            <p>In particular they tend to &quot;gap&quot; to a lesser extent than traditional solid floor boards when exposed to very dry conditions.</p>
            <p>Engineered boards can be fixed using secret nail (at a 45 degree angle through the tongue), face nail or face screw and plug, into plywood over joists or concrete. They can also be laid as floating floors or as fully glued floors. Please refer to <a href="<?php echo $sn; ?>/hardwood-floor-installation">Installation Information</a> and <a href="<?php echo $sn; ?>/disclaimer">Disclaimer &amp; Care</a> sections of this website for details.</p>
